int		ft_sqrt(int nb)
{
	int ope;

	ope = 2;
	if (nb == 1)
		return (1);
	else if (nb > 0)
	{
		while (ope * ope < nb)
			ope++;
		if (ope * ope == nb)
			return (ope);
	}
	return (0);
}
